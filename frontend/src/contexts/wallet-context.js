export function WalletProvider({ children }) {
  const [address, setAddress] = useState(null);

  const web3 = useMemo(
    () =>
      new Web3("https://ropsten.infura.io/v3/80a9f3d8a5f644de834bb4b36dfa2103"),
    []
  );

  const connectWallet = useCallback(async () => {
    if (!window.ethereum || !window.ethereum.isMetaMask) {
      window.alert(`Metamask Wallet Required`);
      window.open(
        `https://chrome.google.com/webstore/detail/metamask/nkbihfbeogaeaoehlefnkodbefgpgknn`
      );
      return;
    }

    const chainId = await window.ethereum.request({ method: "eth_chainId" });

    // ropsten
    if (chainId !== "0x3") {
      await window.ethereum.request({
        method: "wallet_switchEthereumChain",
        params: [
          {
            chainId: "0x3",
          },
        ],
      });
    }

    const [address] = await window.ethereum.request({
      method: "eth_requestAccounts",
    });

    if (!address) {
      window.alert("something went wrong");
      return;
    }

    setAddress(address);
  }, []);

  const disconnectWallet = useCallback(() => {
    setAddress(null);
  }, []);

  const callContract = useCallback(
    async ({ methodName, params }) => {
      return new web3.eth.Contract(ABI, CONTRACT_ADDRESS).methods[methodName](
        ...params
      ).call();
    },
    [web3]
  );

  const value = useMemo(
    () => ({
      address,
      connectWallet,
      disconnectWallet,
      callContract,
    }),
    [address, connectWallet, disconnectWallet, callContract]
  );

  return <Context.Provider value={value}>{children}</Context.Provider>;
}

export function useWalletContext() {
  return useContext(Context);
}
